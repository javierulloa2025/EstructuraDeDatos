""" funciones lambda (también llamadas funciones anónimas) 
Son funciones pequeñas y concisas que se definen sin un nombre (aunque pueden asignarse a una variable). 
Se crean usando la palabra clave lambda y son útiles para operaciones simples que pueden expresarse en una sola línea.
"""
suma = lambda a, b: (a + b)
potencia = lambda a, b: (pow(a, b))

print(suma(2,3))
print(potencia(2,4))

